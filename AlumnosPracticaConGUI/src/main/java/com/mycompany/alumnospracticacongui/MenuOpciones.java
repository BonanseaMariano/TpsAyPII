    public static int busq(){
        int x,j;
        int i=-1;
        
        x=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el legajo del alumno a buscar"));
        
        for (j = 0; j < AlumnosPractica.n && x!=AlumnosPractica.veca[j].legajo; j++);
            if (j==AlumnosPractica.n) {
                JOptionPane.showMessageDialog(null, "El legajo no pudo ser encontrado en la base de datos");
            }else{
                i=j;
            }     
            return i;
        }
    
