    private void M5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_M5ActionPerformed
        b=busq();
        if (b!=-1) {
            JOptionPane.showMessageDialog(null, "El alumno fue encontrado, se eliminaran sus datos:");
             for (int i = b; i < AlumnosPractica.n; i++) {
                 AlumnosPractica.veca[i]=AlumnosPractica.veca[i+1];
             }
             AlumnosPractica.n--;
             AlumnosPractica.modelo.removeRow(b);
             tmd.TodosDatos();
             tmd.setVisible(true);
        }     
    }//GEN-LAST:event_M5ActionPerformed

    private void M6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_M6ActionPerformed
        int m=0;
        int f=0;
        
        for (int i = 0; i < AlumnosPractica.n; i++) {
            if (AlumnosPractica.veca[i].getGenero().compareToIgnoreCase("Masculino")==0) {
                m++;
            }
            if (AlumnosPractica.veca[i].getGenero().compareToIgnoreCase("Femenino")==0) {
                f++;
            }
        }
        JOptionPane.showMessageDialog(null, "Cantidad de alumnos genero masculino: "+m+"\nCantidad de alumnos genero femenino: "+f,"Cantidad de alumnos por genero",1);
    }//GEN-LAST:event_M6ActionPerformed

    private void M7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_M7ActionPerformed
        String m="";
        Alumno aux= new Alumno();
        for (int i = 0; i < AlumnosPractica.n-1; i++) {
            for (int j = i+1; j < AlumnosPractica.n; j++) {
                if (AlumnosPractica.veca[i].getLegajo()>AlumnosPractica.veca[j].getLegajo()) {
                    aux=AlumnosPractica.veca[i];
                    AlumnosPractica.veca[i]=AlumnosPractica.veca[j];
                    AlumnosPractica.veca[j]=aux;
                }
            }
        }
        tmd.TodosDatos();
        tmd.setVisible(true);
    }//GEN-LAST:event_M7ActionPerformed

