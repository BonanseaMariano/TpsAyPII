    private void VisualKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_VisualKeyPressed
        int t=evt.getKeyCode();
        if ((t>=evt.VK_0 && t<=evt.VK_9) || ( t>=evt.VK_NUMPAD0 && t<=evt.VK_NUMPAD9) || t==evt.VK_BACK_SPACE) {
            Visual.setEditable(true);
            Visual.setBackground(Color.white);
        }else{
            Visual.setEditable(false);
            Visual.setBackground(Color.red);
        }
    }//GEN-LAST:event_VisualKeyPressed
    
