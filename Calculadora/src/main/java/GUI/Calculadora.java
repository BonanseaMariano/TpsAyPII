    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        num2= Double.parseDouble(Visual.getText());
        double s0;
        
        switch (op) {
            case "+":
                Visual.setText(Double.toString(num1+num2));
                break;
            case "-":
                Visual.setText(Double.toString(num1-num2));
                break;
            case "*":
                Visual.setText(Double.toString(num1*num2));
                break;
            case "/":
                if (num2==0) {
                   Visual.setText("No se puede dividir entre 0"); 
                }else{
                    Visual.setText(Double.toString(num1/num2));
                }
                break;
            case "pow":
                Visual.setText(Double.toString(Math.pow(num1, num2)));
                break;
        }
        
        s0=Double.parseDouble(Visual.getText());
        if (s0%1==0) {
            Visual.setText(Visual.getText().substring(0, Visual.getText().length()-2)); 
        }
    }//GEN-LAST:event_CalcularActionPerformed

