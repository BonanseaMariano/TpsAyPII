package figura;

/**
 *
 * @author Mariano
 */
public class Punto {
    private int x;
    private int y;
    private int z;

    public Punto(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public Punto(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Punto{" + "x=" + x + ", y=" + y + '}';
    }
    
}
